/*
 * Kill-switch service worker.
 * The previous Jekyll site registered a caching service worker at this URL;
 * this replacement wipes its caches and unregisters itself so returning
 * visitors get the live site again. Keep it around indefinitely.
 */
self.addEventListener('install', () => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      await Promise.all(keys.map((key) => caches.delete(key)));
      await self.registration.unregister();
      const clients = await self.clients.matchAll({ type: 'window' });
      clients.forEach((client) => client.navigate(client.url));
    })()
  );
});
